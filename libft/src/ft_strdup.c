#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*tab;

	i = 0;
	while (s[i] != '\0')
		i++;
	tab = malloc(sizeof(char) * (i + 1));
	if (!tab)
		return (NULL);
	i = 0;
	while (s[i])
	{
		tab[i] = s[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
